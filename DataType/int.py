# 整数(int)
# 整数(int)は下記の様に記述します。

num = 1234        # 正の整数 (10進数)

# 0o(0O), 0x(0X), 0b(0B) はそれぞれ、8進数、16進数、2進数を意味します。

num2 = 0b11000100   #  2進数 (0b/0Bで始まる数値は 2進数とみなされる)
num8 = 0o777        #  8進数 (0o/0Oで始まる数値は 8進数とみなされる)
num16 = 0xffff       # 16進数 (0x/0Xで始まる数値は16進数とみなされる)

print(num)
print(num2)
print(num8)
print(num16)